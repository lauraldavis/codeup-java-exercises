public class StringExercise {

    public static void main(String[] args) {
        // 1. String Basics
        // For each output example create a String variable to contains output & print it out to the console (w/1 variable, 1 print statement).

        String pinkFloydQuote = "We don't need no education\nWe don't need no thought control";
        System.out.println(pinkFloydQuote);

        String quoteExample = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(quoteExample);

        String backslashExample = "In windows, the main drive is usually C:\\";
        System.out.println(backslashExample);

        String backslashesExample = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(backslashesExample);

    }
}
